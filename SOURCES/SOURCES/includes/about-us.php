    <h2>À propos</h2>

    <p>
    	Judith Lossmann, éditrice indépendante est une passionnée de voyages, d'écriture et de partage. On lui doit l'édition d'un beau livre, véritable référence chez Disney : "From Sketch to Reality" qui a dépassé les 20.000 exemplaires vendus, la production clef en mains de magazines internes des groupes Eurosites et Cap Gemini Ernst and Young, la publication de livres tels que "Émois et Moi" ou "Oui, 22 juin".
    </p>

	<p>
		Dans le milieu du tourisme, on la connaît comme fondatrice du magazine La Vie Est Belle VOYAGES, le tout premier magazine entièrement consacré aux voyages et à l'art de vivre, diffusé au format FSE™ ( Feuilletable-Sur-Écrans ) en France dès 2009 auquel s'ajoute un site, du même nom, qui réunit 30.000 visiteurs uniques par mois.
	</p>

	<p>
		Aujourd'hui, Judith Lossmann, forte de son expertise, se lance un nouveau défi : créer un magazine print, un mook consacré aux voyages intitulé : Une Année de Voyages.
	</p>

	<h3>Qu’est-ce qu'un MOOK ?</h3>

	<p>
		Selon Wikipédia, un mook (contraction de magazine et book) est un ouvrage hybride dont le format se situe entre le magazine et le livre. Il est publié de manière périodique et renferme essentiellement de grands reportages. Il comprend également des dessins et des photos servant à l’illustration des textes.
	</p>


